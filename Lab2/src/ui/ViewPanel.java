    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) TableVital.getModel();
        model.setRowCount(0);
        for (Vitals vs : history.getHistory()){
            Object[] row = new Object[3];
            row[0] = vs;
            row[1] = vs.getTemperature();
            row[2] = vs.getPulse();
            
            model.addRow(row);
        }  
        
    }

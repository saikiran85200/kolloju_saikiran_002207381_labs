    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = TableVital.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(this,"Please select a row to delete");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) TableVital.getModel();
        Vitals selected = (Vitals) model.getValueAt(selectedRow, 0);
        txtTemp.setText(String.valueOf(selected.getTemperature()));
        txtBlood.setText(String.valueOf(selected.getBloodPressure()));
        txtPulse.setText(String.valueOf(selected.getPulse()));
        txtDate.setText(selected.getDate());
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = TableVital.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(this,"Please select a row to delete");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) TableVital.getModel();
        Vitals selected = (Vitals) model.getValueAt(selectedRow, 0);
        history.deleteVital(selected);
        JOptionPane.showMessageDialog(this, "Vitals Deleted");
        populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

